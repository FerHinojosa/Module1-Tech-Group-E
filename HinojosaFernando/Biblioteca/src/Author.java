public class Author {
    private String name;
    private int codeAuthor;

    public String getName(){
        return name;
    }
    public void setName (String fname){
        this.name = fname;
    }
    public int getCodeAuthor(){
        return codeAuthor;
    }

    public void setCodeAuthor(int codeAuthor) {
        this.codeAuthor = codeAuthor;
    }
}
